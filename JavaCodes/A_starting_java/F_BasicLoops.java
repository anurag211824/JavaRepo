package JavaCodes.A_starting_java;

public class F_BasicLoops {
    public static void main(String[] args) {
        int cnt=1;
        //while loop
        while(cnt!=6){
            System.out.print(cnt+" ");
            cnt++;
        }
        System.out.println();
        //for loop
        for(int i=1;i<=5;i++){
            System.out.print(i+" ");
        }
    }
}

